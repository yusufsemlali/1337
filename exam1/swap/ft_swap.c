void ft_swap(int *a, int *b)
{
	int tmp;
	tmp = *a;
	*a = *b;
	*b  = tmp;
}
int main(void)
{
	int i = 123;
	int i2 = 321;
	ft_swap(&i,&i2);
	printf("%d  %d", i, i2);
	return 0;
}
